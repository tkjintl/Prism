// Apply small per-instance variation so 400 deals from 20 templates look distinct.
// seed should be the deal index (or any number); makes results pseudo-deterministic-ish
// but still varied across the run.
export function randomizeDeal(template, seed = 0) {
  const r = (off = 0) => {
    // Cheap deterministic-ish PRNG seeded by (seed + off)
    const x = Math.sin((seed + off) * 9999) * 10000;
    return x - Math.floor(x);
  };
  // ±15% on raise size
  const sizeMul = 0.85 + r(1) * 0.30;
  const target_alloc_usd = Math.round((template.target_alloc_usd * sizeMul) / 1e5) * 1e5;

  // ±2 percentage points on IRR
  const target_irr = Math.max(5, Math.round((template.target_irr + (r(2) - 0.5) * 4) * 10) / 10);

  // Min ticket variations: pick from a realistic ladder
  const ticketLadder = [250000, 500000, 750000, 1000000, 2000000];
  const min_ticket_usd = ticketLadder[Math.floor(r(3) * ticketLadder.length)];

  // Term ±12 months
  const term_months = Math.max(24, Math.round(template.term_months + (r(4) - 0.5) * 24));

  // Vintage suffix on name to keep instances distinct
  const vintageSuffixes = ['', ' (2026 Vintage)', ' Series A', ' — Co-Invest Sleeve', ' SP', ' II', ' Tranche B'];
  const nameSuffix = seed > 0 ? vintageSuffixes[seed % vintageSuffixes.length] : '';

  // Slightly varied tagline
  const taglineVariants = template.tagline ? [
    template.tagline,
    template.tagline + ' — institutional sleeve',
    template.tagline + ' (closed-end vintage)',
    'Aurum-curated: ' + template.tagline,
  ] : [];
  const tagline = taglineVariants.length ? taglineVariants[Math.floor(r(5) * taglineVariants.length)] : '';

  return {
    ...template,
    name: template.name + nameSuffix,
    target_alloc_usd,
    target_irr,
    min_ticket_usd,
    term_months,
    tagline,
  };
}
